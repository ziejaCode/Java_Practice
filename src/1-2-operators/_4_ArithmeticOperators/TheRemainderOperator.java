package _4_ArithmeticOperators;

public class TheRemainderOperator {

	/**
	    One operator you might not be as familiar with is the remainder( pozostalosc ) operator, %. The
		remainder operator divides the left operand by the right operand, and the result is
		the remainder
	 */
	
	public static void main(String[] args) {
		int x = 15;
		int y = x % 4;
		System.out.println("The result of 15 % 4 is the "
		+ "remainder of 15 divided by 4. The remainder is " + y);
	}

}
